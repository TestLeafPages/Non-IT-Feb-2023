package week1.day2;

public class LearnLooping {

	public static void main(String[] args) {
	
		System.out.println(1);
		System.out.println(2);
		System.out.println(3);
		System.out.println(4);
		//increment
		for (int i = 0; i <=5; i++) {
			System.out.println(i);
		}
		//decrement
		for (int i =10; i>=0; i--) {
			System.out.println(i);
		}
		//print the name in 5 times
		for (int i = 0; i <5; i++) {
			System.out.println("Testleaf");
			
		}
		int a=1;
		while(a<6) {
			System.out.println("Hello Testleaf");
			a++;
		}
		
		
		
		
		
		
		
		
		
}
}
